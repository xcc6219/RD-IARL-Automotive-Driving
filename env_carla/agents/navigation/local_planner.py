"""
Minimal local_planner module providing only the RoadOption enum,
which is required by the GlobalRoutePlanner.
"""

from enum import Enum


class RoadOption(Enum):
    VOID = -1
    LEFT = 1
    RIGHT = 2
    STRAIGHT = 3
    LANEFOLLOW = 4
    CHANGELANELEFT = 5
    CHANGELANERIGHT = 6
